#include<stdio.h>
/*
假设一对兔子出生三个月后开始每月生一对小兔，兔子不死，现有一对兔子，求一年内每月兔子的总数
*/
void main()
{
	int num[24] = {1,1};//由题，1，2月份就1对 
	int i = 0;
	for(i = 0; i < 24; i++)//用i表月份（题目不复杂，不必要用多么醒目易理解的标识符）
	{
		if(i == 0 || i == 1)
			printf("第%d月，兔子总数:%d\n",i+1,num[i]);
		else
		{
			num[i] = num[i-1]+num[i-2];		//本题关键（递归/迭代）
			printf("第%d月，兔子总数:%d\n",i+1,num[i]);
		}
		
	}
}